#include <stdio.h>
#include <Windows.h>

void textColor(int colorNum);
void gotoxy(int x, int y);
void drawGameBoard();

enum color {
};
void textColor(int colorNum) {
    SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), colorNum);
}


void gotoxy(int x, int y) {
    COORD pos = { x*2, y };
    SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), pos);
}

void drawGameBoard() {
    textColor(1);
}

int main() {
    drawGameBoard();
}

